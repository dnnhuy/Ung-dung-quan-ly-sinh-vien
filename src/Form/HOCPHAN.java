    private void initcbbMaKhoa() {
        try {
            String sql = "Select MaKhoa from Khoa";
            Connection conn = Connect.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            cbbMaKhoa.removeAllItems();
            while (rs.next()) {
                cbbMaKhoa.addItem(rs.getString("MaKhoa"));
            }
            rs.close();
            pm.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }
     public void Loadulieu() {

        try {
            tbl_hocphan.removeAll();
            String[] arr = {"Mã học phần", "Tên HP", "Số TC","Số tiết LT","Số tiết TH","Mã khoa"};
            tbtModel = new DefaultTableModel(arr, 0);
            String sql = "SELECT * FROM HocPhan";
            Connection conn = Connect.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]
            {
                    rs.getString("MaHP"), rs.getString("TenHP"),
                    rs.getString("SoTinChi"), rs.getString("SoTietLT"),
                    rs.getString("SoTietTH"),rs.getString("MaKhoa")
                };
                
                tbtModel.addRow(row);
            }
            tbl_hocphan.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            Connect.CloseConnect(conn);
        } catch (Exception e) {
           e.printStackTrace();
        }
    }

    
