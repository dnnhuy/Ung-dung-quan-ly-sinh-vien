    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaHP.getText().equals(""))
        {
            sb.append("MaHP đang trống!\n");
            txtMaHP.requestFocus();
        }
        if (sb.length() > 0)
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            Hocphan HP = new Hocphan();
            HOCPHANDAO HocPhanup = new  HOCPHANDAO();

            HP.setMaHP(txtMaHP.getText());
            HP.setTenHP((txtTenHP.getText()));
            HP.setSoTinChi(txtSoTinChi.getText());
            HP.setSoTietLT(txtSoTietLT.getText());
            HP.setSoTietTH((txtSoTietTH.getText()));
            String n1 = cbbMaKhoa.getItemAt(cbbMaKhoa.getSelectedIndex());
            HP.setMaKhoa(n1);

            HocPhanup.Update(HP);
            Loadulieu();
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thành công!");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Cập nhật không thành công!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

