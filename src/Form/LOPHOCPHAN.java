        int lhp = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa lớp học phần này", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (lhp == JOptionPane.YES_OPTION) {
            try {
                LOPHOCPHANDAO lhpd = new LOPHOCPHANDAO();
                if (lhpd.Delete(txtMaLHP.getText())) {
                    JOptionPane.showMessageDialog(this, "Xóa lớp học phần thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa lớp học phần thất bại");
                }
                Loadulieu();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
