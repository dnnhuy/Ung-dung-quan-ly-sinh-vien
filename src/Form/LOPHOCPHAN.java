    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaLHP.getText().equals(""))
        {
            sb.append("MaLHP đang trống!\n");
            txtMaLHP.requestFocus();
        }
        if (sb.length() > 0)
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            LopHP lhp = new LopHP();
            LOPHOCPHANDAO LopHPup = new LOPHOCPHANDAO();

            lhp.setMaLHP(txtMaLHP.getText());
            lhp.setTenHP((txtTenHP.getText()));
            lhp.setNgayBd(txtNgayBd.getText());
            lhp.setNgayKt(txtNgayKt.getText());
            lhp.setNgayDukienthi((txtNgayDukienthi.getText()));
            String n = cbbHK.getItemAt(cbbHK.getSelectedIndex());
                    lhp.setHK(n);
            lhp.setNam(txtNam.getText());
            String n2 = cbbMaHP.getItemAt(cbbMaHP.getSelectedIndex());
            lhp.setMaHP(n2);
            String n1 = cbbMaGV.getItemAt(cbbMaGV.getSelectedIndex());
            lhp.setMaGV(n1);

            LopHPup.Update(lhp);
            Loadulieu();
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thành công!");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Cập nhật không thành công!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

