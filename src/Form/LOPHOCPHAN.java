     public void Loadulieu() {

        try {
            tbl_lophp.removeAll();
            String[] arr = {"Mã lớp học phần HP", "Tên học phần", "Ngày bắt đầu","Ngày kết thúc","Ngày dự kiến thi","Học kỳ","Năm","Mã HP","Mã GV"};
            tbtModel = new DefaultTableModel(arr, 0);
            String sql = "SELECT * FROM LopHP";
            Connection conn = Connect.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]
            {
                    rs.getString("MaLHP"),        rs.getString("TenHP"),
                    rs.getString("NgayBd"),       rs.getString("NgayKt"),
                    rs.getString("NgayDukienthi"),rs.getString("HK"),
                    rs.getString("Nam"),          rs.getString("MaHP"),
                    rs.getString("MaGV")
                };
                
                tbtModel.addRow(row);
            }
            tbl_lophp.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            Connect.CloseConnect(conn);
        } catch (Exception e) {
           e.printStackTrace();
        }
    }
