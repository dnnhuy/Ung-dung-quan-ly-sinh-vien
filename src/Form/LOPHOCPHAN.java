     private void initcbbMaHP() {
        try {
            String sql = "Select MaHP from HocPhan";
            Connection conn = Connect.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            cbbMaHP.removeAllItems();
            while (rs.next()) {
                cbbMaHP.addItem(rs.getString("MaHP"));
            }
            rs.close();
            pm.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }
    
